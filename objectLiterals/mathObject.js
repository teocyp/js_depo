console.log(Math.E); // Euler's number


const area = 7.7;

console.log(Math.round(area)); // Rounds the number to the nearest integer(number)
console.log(Math.floor(area)); // Floors the number down to the nearest integer
console.log(Math.ceil(area)); // Ceils up the number to the nearest integer
console.log(Math.trunc(area)); // It removes the decimal and leaves the integer



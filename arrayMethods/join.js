// join() creates and returns a new string by concatenating all of the elements in an array. 


const arr = ['Teo', 'loves', 'Javascript', '.'];

console.log(arr.join()); // outputs Teo,loves,Javascript.
console.log(arr.join('')); // outputs TeolovesJavascript.
console.log(arr.join(' ')); // outputs Teo loves Javascript .
console.log(arr.join('-')); // outputs Teo-loves-Javascript-.
